Use this page to manage your team roster for the selected season.<br/><br/>
<h4>Create the Roster</h4>
To add a player to your roster, use the button at the bottom of the page, "Add Player".<br/>
A player may already exist in the database, or may be added as a new player to TMSL.<br/>
You may add a player to your team provided several criteria are met:
<ul>
<li>The player is not already on another team for the season (he MAY be on an over-45 and an open division team simultaneously)</li>
<li>The player meets the age requirements for the league</li>
<li>The last day to add a player has not passed</li>
<li>The number of players on the team does not exceed the maximum number allowed</li>
</ul>
To drop a player from your roster, click the
<img alt='Drop' src='images/delete.png' title='Drop' border='0'> icon.<br/>
To edit information about a player, such as address, etc., use the
<img alt='Edit' src='images/pencil.png' title='Edit' border='0'> icon by the player's name.<br/>
<h4>Register the Team</h4>
When you're ready to register your team, provided you have at least the minimum number of players on your roster, and the last day to register hasn't passed,
a "REGISTER TEAM" button will appear.  Click that to submit your registration, and instructions will appear as to costs, and how to submit payment.<br/>
After payment has been received by TMSL, the team will be marked as registered and can play.<br/>
Players added after the roster has been submitted must submit payment to be able to play.<br/>
All players must have a photo on file -- if you see the camera
<img alt='Camera' src='images/camera.png' border='0'> icon, you will need to upload a jpeg image of that player.<br/>

Once a team's registration is accepted, registered players will appear in white rows.  A gray row means the user's registration has not been accepted yet.<br/>
A yellow row indicates the player has been suspended by the D&R committee.<br/>
